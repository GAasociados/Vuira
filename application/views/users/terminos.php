La Administración Pública Municipal de Irapuato, Guanajuato protegerá tus datos personales recolectados a través del portal irapuato.gob.mx, en los sucesivo irapuato.gob.mx, por esta razón, se da a conocer a sus usuarios las siguientes políticas, basadas en la normatividad vigente aplicable a la protección de datos personales.
<br>
<br>
<h5><b>Objeto.</b></h5>
La Política de Privacidad y Protección de Datos Personales, en adelante la Política de Privacidad, explica cómo se tratan y protegen los datos personales que sean recolectados en el portal irapuato.gob.mx; dándote la seguridad de que tus datos serán almacenados en plataformas seguras.
<br>
<br>
<h5><b>Responsable del tratamiento de los datos personales y fundamento para ello.</b></h5>
La Unidad de Gobierno Digital de la Secretaría de la Función Pública, en adelante UGD, hace de tu conocimiento que el fundamento para administrar la información en el portal www.irapuato.gob.mx, se encuentra en los artículos 6° y 16 segundo párrafo de la Constitución Política de los Estados Unidos Mexicanos; así como en el artículo 37, fracción XXVI, en relación con el segundo transitorio de la Ley Orgánica de la Administración Pública Federal; el artículo 18, fracciones I, VI, VII, VIII, IX, X y XIII del Reglamento Interior de la Secretaría de la Función Pública, Ley General de Transparencia y Acceso a la Información Pública, Lineamientos de Protección de Datos Personales, el artículo 20 de la Ley Federal de Transparencia y Acceso a la Información Pública Gubernamental; décimo sexto y décimo séptimo de los Lineamientos de Protección de Datos Personales.
<br>
<br>
Los usuarios deben saber que al acceder a este portal pueden ser direccionados a otros sitios que están fuera de nuestro control. La Secretaría de la Función Pública no endosa, representa ni garantiza la Política de Privacidad o el contenido de ningún sitio de terceros que tenga enlaces desde o hacia el sitio irapuato.gob.mx
<br><br>
<h5><b>Finalidades del tratamiento de los datos personales.</b></h5>

Los datos personales podrán ser tratados para los siguientes fines:
<br>

<li style="list-style:none;">a) Realizar alguno de los trámites digitalizados disponibles;</li>
<li style="list-style:none;">b) Habilitar el sistema para que puedas hacer comentarios y/o agenda de citas;</li>
<li style="list-style:none;">c) Realizar estadísticas que nos permitan mejorar tu experiencia como usuario;</li>
<li style="list-style:none;">d) Otras previstas en la Ley.</li>

<br>
La Política de Privacidad, explica cómo se tratan y protegen los datos personales que sean recolectados en el portal irapuato.gob.mx; dándote la seguridad de que tus datos serán almacenados en plataformas seguras.
<br>
<br>
<h5><b>Consentimiento en materia de protección de datos personales.</b></h5>

Los datos personales podrán ser tratados sin consentimiento del titular, siempre en respeto a los derechos de los usuarios, y sólo por razones de seguridad nacional, disposiciones de orden público, seguridad y salud públicas o para proteger los derechos de terceros, según lo establece de esta manera, el segundo párrafo del artículo 16, de la Constitución Política de los Estados Unidos Mexicanos, así como en los supuestos previstos por el artículo 22 de la Ley Federal de Transparencia y Acceso a la Información Pública Gubernamental.
<br>
<br>
<h5><b>Uso de cookies.</b></h5>
El portal irapuato.gob.mx utiliza cookies para ayudarnos a obtener información estadística sobre el uso del portal. En cualquier momento puede eliminar la cookie que utiliza nuestro portal, accediendo a las preferencias de configuración de su navegador. Igualmente puede impedir que vuelva a instalarse dicho archivo, realizando los ajustes correspondientes en las preferencias de configuración de su navegador.
<br>
<br>
<h5><b>Cambios y actualizaciones a la Política de Privacidad.</b></h5>

La presente política de privacidad puede cambiar o actualizarse periódicamente; por lo que te pedimos la revises constantemente y puedas estar al tanto de la última versión que rige el tratamiento de tus datos personales. No obstante lo anterior, la presente política de privacidad siempre deberá observar las disposiciones jurídicas aplicables.
<br>
<br>
<h5><b>Acceso y corrección de tus datos personales.</b></h5>
La Administración Pública Municipal, deberá permitir al interesado ejercer los derechos de acceso y corrección de datos personales de conformidad con el artículo 16 párrafo segundo de la Constitución Política de los Estados Unidos Mexicanos, así como los artículos 24 y 25 de la Ley Federal de Transparencia y Acceso a la Información Pública Gubernamental, así como el 47 de su Reglamento, y conforme a los procedimientos y mecanismos previstos en los lineamientos emitidos en esa materia por el INAI. Lo anterior también, en cumplimiento del Décimo Séptimo y Décimo Octavo de los Lineamientos de Protección de Datos Personales.
<br><br>
Para ejercer los derechos de acceso y corrección, así como para realizar una denuncia, queja, reporte o sugerencia, podrá:
